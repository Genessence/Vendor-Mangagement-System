// Utility functions
export const getPhoneCode = (countryCode) => {
  const phoneInfo = countryPhoneCodes.find(p => p.country === countryCode);
  return phoneInfo ? phoneInfo.code : '';
};

export const getPhoneCodeWithName = (countryCode) => {
  const phoneInfo = countryPhoneCodes.find(p => p.country === countryCode);
  return phoneInfo ? { code: phoneInfo.code, name: phoneInfo.name } : null;
};

export const formatPhoneNumber = (countryCode, phoneNumber) => {
  const phoneCode = getPhoneCode(countryCode);
  if (!phoneCode || !phoneNumber) return phoneNumber;
  
  // If the phone number already starts with the country code, return as is
  if (phoneNumber.startsWith(phoneCode)) {
    return phoneNumber;
  }
  
  // If the phone number starts with a different country code, remove it
  const cleanNumber = phoneNumber.replace(/^\+?\d{1,4}/, '');
  
  // If the clean number is empty, return empty
  if (!cleanNumber.trim()) {
    return '';
  }
  
  // Return formatted number with country code
  return `${phoneCode} ${cleanNumber}`;
};

export const getPhonePlaceholder = (countryCode) => {
  const phoneCode = getPhoneCode(countryCode);
  if (!phoneCode) return 'Enter phone number';
  
  // Return appropriate placeholder based on country
  switch (countryCode) {
    case 'IN': // India
      return `${phoneCode} 98765 43210`;
    case 'US': // United States
      return `${phoneCode} (555) 123-4567`;
    case 'GB': // United Kingdom
      return `${phoneCode} 7700 900000`;
    case 'CA': // Canada
      return `${phoneCode} (555) 123-4567`;
    case 'AU': // Australia
      return `${phoneCode} 412 345 678`;
    default:
      return `${phoneCode} Enter phone number`;
  }
}; 